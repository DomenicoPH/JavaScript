// Variables

// Incrementar valor de una variable:
var librosComprados = 105;
console.log(librosComprados);
// + 1
librosComprados = librosComprados + 1;
console.log(librosComprados);
// + 1
librosComprados++;
console.log(librosComprados);
// + 1
librosComprados += 1;
//asignación de SUMA +=
console.log(librosComprados);


// Decrementar valor de una variable:
var numeroDeEstudiantes = 500;
console.log(numeroDeEstudiantes);
// - 1
numeroDeEstudiantes = numeroDeEstudiantes - 1;
console.log(numeroDeEstudiantes);
// - 1
numeroDeEstudiantes--;
console.log(numeroDeEstudiantes);
// - 1
numeroDeEstudiantes -= 1;
// asignación de RESTA -=
console.log(numeroDeEstudiantes);


// Asignación de Suma:
var ventaDelDia = 1535.48;
console.log('Venta del día: ', ventaDelDia);
var ventaDeUltimoMinuto = 499.99;
ventaDelDia += ventaDeUltimoMinuto;
console.log('Venta del día actualizada: ', ventaDelDia);


// Asignación de Resta:
var totalDeuda = 2446
console.log('Deuda total: ', totalDeuda);
var ingresoExtra = 500;
totalDeuda -= ingresoExtra;
console.log('Deuda total actualizada: ', totalDeuda);


